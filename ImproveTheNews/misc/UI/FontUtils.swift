import Foundation
import UIKit



func MERRIWEATHER_BOLD(_ size: CGFloat = 10.0) -> UIFont {
    return UIFont(name: "Merriweather-Bold", size: size)!
}

func MERRIWEATHER(_ size: CGFloat = 10.0) -> UIFont {
    return UIFont(name: "Merriweather", size: size)!
}

func ROBOTO(_ size: CGFloat = 10.0) -> UIFont {
    return UIFont(name: "Roboto-Regular", size: size)!
}

func ROBOTO_BOLD(_ size: CGFloat = 10.0) -> UIFont {
    return UIFont(name: "Roboto-Bold", size: size)!
}

func AILERON(_ size: CGFloat = 10.0) -> UIFont {
    return UIFont(name: "Aileron-Regular", size: size)!
}

func AILERON_SEMIBOLD(_ size: CGFloat = 10.0) -> UIFont {
    return UIFont(name: "Aileron-SemiBold", size: size)!
}

func AILERON_BOLD(_ size: CGFloat = 10.0) -> UIFont {
    return UIFont(name: "Aileron-Bold", size: size)!
}

func DM_SERIF_DISPLAY(_ size: CGFloat = 10.0) -> UIFont {
    return UIFont(name: "DMSerifDisplay-Regular", size: size)!
}

func DM_SERIF_DISPLAY_fixed(_ size: CGFloat = 10.0) -> UIFont {
    return UIFont(name: "DMSerifDisplay-Regular", size: size+3)!
}
